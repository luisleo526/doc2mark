class TestContentFidelity:
    """Page/slide/sheet markers should not alter document text."""

    def test_pdf_no_marker_inside_paragraph(self):
        result = _load("test_rag_features.pdf")
        # Markers should be on their own lines, not inside paragraphs
        for line in result.content.split("\n"):
            if "<!--" in line:
                stripped = line.strip()
                assert stripped.startswith("<!--") and stripped.endswith("-->"), \
                    f"Marker should be standalone: '{stripped}'"

    def test_docx_markdown_parseable(self):
        """Output should be valid markdown (no broken syntax)."""
        result = _load("test_rag_features.docx")
        # Check heading syntax
        for line in result.content.split("\n"):
            if line.startswith("#"):
                assert re.match(r'^#{1,6}\s', line), f"Invalid heading: '{line}'"

    def test_pptx_content_not_duplicated(self):
        """Slide content should not be duplicated across slides."""
        result = _load("test_rag_features.pptx")
        count = result.content.count("Quarterly Business Review")
        assert count == 1, f"Title duplicated {count} times"

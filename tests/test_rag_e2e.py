class TestChunkingE2E:
    """Chunking real documents should produce meaningful chunks."""

    def test_pdf_chunks_have_page_ranges(self):
        result = _load("test_rag_features.pdf")
        chunks = result.get_chunks(ChunkingConfig(max_chunk_size=500, overlap=0))
        assert len(chunks) >= 2, "Multi-page PDF should produce multiple chunks"
        for chunk in chunks:
            if chunk.page_start is not None:
                assert chunk.page_start >= 1
                assert chunk.page_end >= chunk.page_start

    def test_pdf_chunks_cover_all_content(self):
        result = _load("test_rag_features.pdf")
        chunks = result.get_chunks(ChunkingConfig(max_chunk_size=500, overlap=0))
        all_text = " ".join(c.content for c in chunks)
        assert "Executive Summary" in all_text
        assert "Market Analysis" in all_text
        assert "Revenue grew" in all_text

    def test_pdf_headers_excluded_from_chunks(self):
        result = _load("test_rag_features.pdf")
        chunks = result.get_chunks(ChunkingConfig(max_chunk_size=10000, overlap=0))
        all_text = " ".join(c.content for c in chunks)
        assert "ACME Corp" not in all_text, "Header should not appear in chunks"

    def test_docx_chunks_have_sections(self):
        result = _load("test_rag_features.docx")
        chunks = result.get_chunks(ChunkingConfig(max_chunk_size=500, overlap=0))
        titles = [c.section_title for c in chunks if c.section_title]
        assert "Introduction" in titles
        assert "Methodology" in titles

    def test_docx_footnote_travels_with_chunk(self):
        result = _load("test_rag_features.docx")
        chunks = result.get_chunks(ChunkingConfig(max_chunk_size=10000, overlap=0))
        # The chunk containing [^1] reference should also have [^1]: definition
        for chunk in chunks:
            if "[^1]" in chunk.content and "[^1]:" not in chunk.content.split("[^1]")[0]:
                # This chunk references footnote 1
                assert "[^1]:" in chunk.content, \
                    "Footnote definition should be in same chunk as reference"
                break

    def test_xlsx_chunks_contain_table_data(self):
        result = _load("test_rag_features.xlsx")
        chunks = result.get_chunks()
        all_text = " ".join(c.content for c in chunks)
        assert "Widget A" in all_text

    def test_chunk_indices_sequential(self):
        result = _load("test_rag_features.pdf")
        chunks = result.get_chunks(ChunkingConfig(max_chunk_size=300, overlap=0))
        for i, chunk in enumerate(chunks):
            assert chunk.chunk_index == i



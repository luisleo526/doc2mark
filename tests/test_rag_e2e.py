class TestDocxHeaderFooter:
    """DOCX headers/footers should be tagged and excluded from body."""

    def test_header_tagged(self):
        result = _load("test_rag_features.docx")
        headers = [i for i in result.json_content if i["type"] == "text:header"]
        assert len(headers) >= 1
        assert any("ACME" in h["content"] or "Confidential" in h["content"] for h in headers)

    def test_footer_tagged(self):
        result = _load("test_rag_features.docx")
        footers = [i for i in result.json_content if i["type"] == "text:footer"]
        assert len(footers) >= 1

    def test_header_not_in_markdown_body(self):
        result = _load("test_rag_features.docx")
        # Headers are in json_content but NOT in the main markdown output
        header_texts = [i["content"] for i in result.json_content if i["type"] == "text:header"]
        for header_text in header_texts:
            if header_text.strip():
                # The exact header text should not appear as a standalone line in markdown
                # (it may appear as part of footnote definitions, which is fine)
                lines = result.content.split("\n")
                normal_lines = [l for l in lines if not l.startswith("[^") and "<!-- " not in l]
                for line in normal_lines:
                    assert header_text not in line, f"Header '{header_text}' found in markdown body"


class TestDocxFootnotes:
    """DOCX footnotes should produce [^N] markers and definitions."""

    def test_footnote_reference_in_text(self):
        result = _load("test_rag_features.docx")
        # The first paragraph should have [^1]
        page1_normal = [i for i in result.json_content
                        if i.get("page") == 1 and i["type"] == "text:normal"]
        assert any("[^1]" in i["content"] for i in page1_normal), \
            "Expected [^1] reference in page 1 text"

    def test_footnote_definition_present(self):
        result = _load("test_rag_features.docx")
        footnotes = [i for i in result.json_content if i["type"] == "text:footnote"]
        assert len(footnotes) >= 1
        fn_texts = [f["content"] for f in footnotes]
        assert any("[^1]:" in t for t in fn_texts), "Expected [^1]: definition"

    def test_footnote_in_markdown_output(self):
        result = _load("test_rag_features.docx")
        assert "[^1]:" in result.content, "Footnote definition should appear in markdown"

    def test_endnote_distinct_from_footnote(self):
        """Endnotes should have 'en' prefix to avoid id collision with footnotes."""
        result = _load("test_rag_features.docx")
        footnotes = [i for i in result.json_content if i["type"] == "text:footnote"]
        ids = [re.match(r"\[\^(\w+)\]:", f["content"]).group(1)
               for f in footnotes if re.match(r"\[\^(\w+)\]:", f["content"])]
        # Should have both "1" (footnote) and "en1" (endnote)
        assert "1" in ids, f"Missing footnote id '1', got {ids}"
        assert "en1" in ids, f"Missing endnote id 'en1', got {ids}"



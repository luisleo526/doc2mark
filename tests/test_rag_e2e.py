FIXTURES = Path(__file__).parent / "fixtures"


def _load(filename, **kwargs):
    """Load a fixture document."""
    path = FIXTURES / filename
    if not path.exists():
        pytest.skip(f"{filename} not found — run tests/fixtures/create_test_docs.py")
    old_key = os.environ.get("OPENAI_API_KEY")
    if not old_key:
        os.environ["OPENAI_API_KEY"] = "sk-test-dummy-not-used"
    try:
        from doc2mark import UnifiedDocumentLoader
        loader = UnifiedDocumentLoader()
        return loader.load(str(path), extract_images=False, ocr_images=False, **kwargs)
    finally:
        if not old_key:
            del os.environ["OPENAI_API_KEY"]


